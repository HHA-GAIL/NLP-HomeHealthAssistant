    private void Btn_ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ConfirmActionPerformed
//        System.out.println("Selected:"+List_RuleName.getSelectedValue());
        try {
            if(List_RuleName.getSelectedValue() == null)
                return;
            DefaultListModel<String> modal2 = new DefaultListModel<>();
            //Select the content of the RuleName
            DefaultTableModel tmp = new GaleDTRulesDAO().getTableModel(List_RuleName.getSelectedValue());
            //to save the content as a temp file
            Vector vct = (Vector)tmp.getDataVector().get(0);
            for(int i = 0; i < tmp.getColumnCount(); i ++){
                if(tmp.getColumnName(i).equals("DTXMLContent"))
                {
                    File dtrules = new File(System.getProperty("user.dir") + "/__TMP.xml");
//                    System.out.println(System.getProperty("user.dir") + "/__TMP.xml");
                    dtrules.createNewFile();
                    FileOutputStream fos = new FileOutputStream(dtrules);
                    fos.write(((String)vct.get(i)).getBytes("utf-8"));
                    fos.close();
                }
            }
            //read the temp xml to DOM and give all the tables to select
            DocumentBuilder builder = DocumentBuilderFactory.newInstance().newDocumentBuilder();
            Document dtDocument = builder.parse(System.getProperty("user.dir") + "/__TMP.xml");
            NodeList dtTables = dtDocument.getElementsByTagName("decision_table");
            for(int i = 0; i < dtTables.getLength(); i++){
                Element dtElement = (Element)dtTables.item(i);
                modal2.addElement(dtElement.getElementsByTagName("table_name").item(0).getTextContent());
            }
            List_Tables.setModel(modal2);
            List_Tables.setEnabled(true);
        } catch (Exception e) {
            e.printStackTrace();
            TA_Content.append("\n"+e.getMessage());
        }
    }//GEN-LAST:event_Btn_ConfirmActionPerformed

    private void List_TablesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_List_TablesValueChanged
        //to get the content value of the selected table name
        try{
            File dtrules = new File(System.getProperty("user.dir") + "/__TMP.xml");
            FileInputStream fis = new FileInputStream(dtrules);
            byte[] content = new byte[fis.available()];
            fis.read(content);
            fis.close();
            String contentString = new String(content,"utf-8");
            String[] DT = contentString.split("<decision_table>");
            String[] DTS = contentString.split("</decision_table>");
            //<table_name></table_name>
            int start = 0;int end = 0;
            for(int i = 0; i < DT.length; i++)
            {
                if(DT[i].contains("<table_name>"+List_Tables.getSelectedValue()+"</table_name>"))
                {
                    start = contentString.indexOf(DT[i]);
                    break;
                }
            }
            for (int i = 0; i < DTS.length; i++) {
                if(DTS[i].contains("<table_name>"+List_Tables.getSelectedValue()+"</table_name>"))
                {
                    end = contentString.indexOf(DTS[i]) + DTS[i].length();
                    break;
                }
            }
            TA_Content.setText(contentString.substring(start, end));
            TA_Content.setCaretPosition(0);
        }catch(Exception e){
            e.printStackTrace();
            TA_Content.append("\n"+e.getMessage());
        }
        
    }//GEN-LAST:event_List_TablesValueChanged

