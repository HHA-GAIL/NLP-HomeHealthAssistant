    private void Btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_SearchActionPerformed
        //initial all the status
        //delete the list and set enable to false
        Btn_Confirm.setEnabled(false);
        Btn_Delete.setEnabled(false);
        List_Tables.removeAll();
        DefaultListModel<String> model1 = new DefaultListModel<>();
        List_RuleName.removeAll();
        List_RuleName.setModel(new DefaultListModel<>());
        TA_Content.setText("");
        List_RuleName.setEnabled(false);
        List_Tables.setEnabled(false);
        //select RuleName here
        String RuleName = TB_RuleName.getText();
        if(RuleName != null){
            GaleDTRulesDAO gdtrdao = new GaleDTRulesDAO();
            DefaultTableModel tmp = gdtrdao.getLikelyRuleNames(RuleName);
            if(tmp.getRowCount() < 1)
            {
                TA_Content.setText("No such Rule Name...\n");
                List_Tables.setModel(model1);
                return;
            }
            for(int i = 0; i < tmp.getRowCount(); i++){
                Vector vct = (Vector)tmp.getDataVector().get(i);
                model1.addElement((String)vct.get(0));
            }
            List_RuleName.setModel(model1);
            List_RuleName.setEnabled(true);
            Btn_Confirm.setEnabled(true);
            Btn_Delete.setEnabled(true);
        }
    }//GEN-LAST:event_Btn_SearchActionPerformed

