    private void Btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_DeleteActionPerformed
        //delete the selected RuleName
        if(List_RuleName.getSelectedValue() == null)
            return;
        String RuleName = List_RuleName.getSelectedValue();
        GaleDTRulesDAO gdtrdao = new GaleDTRulesDAO();
        int n=JOptionPane.showConfirmDialog(null, "Are you willing to delete "+RuleName+"?"
            ,"ATTENTION!",JOptionPane.YES_NO_OPTION);
        if(n == JOptionPane.OK_OPTION){
            if(gdtrdao.deleteDTRules(RuleName))
            {
                TA_Content.setText("Delete done...");
                Btn_Search.doClick();
            }
            else
               TA_Content.setText("Delete failed...");
        }
    }//GEN-LAST:event_Btn_DeleteActionPerformed


